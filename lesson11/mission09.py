import math
x = float(input('Введите число:  '))

print('округление вниз  ', math.floor(x))
print('округление вверх  ', math. ceil(x))
print('модуль числа  ', abs(x))
print('квардатный корень  ', math.sqrt(x))
print('экспонента в степени  ', math.expm1(x))
print('натуральный логарифм  ', math.log(x))
print('логарифм по основанию 2  ', math.log2(x))
print('логарифм по основанию 10  ', math. log10(x))
print('синус и косинус числа  ', math.sin(x), math.cos(x))
